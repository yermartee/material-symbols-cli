import { describe, expect, it } from "vitest";
import { getGenerator } from "../generators/index.js";
import { generateBarrel } from "../generators/barrel.js";
import { toKebab } from "../generators/shared.js";
import type { OutputFormat, Config } from "../types.js";

const makeConfig = (overrides: Partial<Config> = {}): Config => ({
  icons: ["home"],
  outputDir: "src/icons",
  style: "outlined",
  format: "mui",
  variants: [{ fill: 0, weight: 400, grade: 0, opticalSize: 24 }],
  naming: {
    convention: "PascalCase",
    prefix: "",
    suffix: "",
    overrides: {},
  },
  defaultProps: { size: 24, color: "currentColor", className: "" },
  optimize: true,
  useClientDirective: true,
  barrel: true,
  extension: "tsx",
  declarations: false,
  concurrency: 25,
  header: "Auto-generated by material-symbols-cli. Do not edit.",
  ...overrides,
});

const SAMPLE_PATH = '<path d="M240-200h120v-240h240"/>';
const COMPONENT = "HomeIcon";

describe("toKebab", () => {
  it("converts PascalCase to kebab-case", () => {
    expect(toKebab("HomeIcon")).toBe("home-icon");
  });

  it("handles all-lowercase when no lowercase-to-uppercase transition", () => {
    expect(toKebab("SVGIcon")).toBe("svgicon");
  });

  it("handles single word", () => {
    expect(toKebab("Home")).toBe("home");
  });
});

describe("getGenerator", () => {
  const allFormats: OutputFormat[] = [
    "mui",
    "react",
    "react-native",
    "vue",
    "angular",
    "svelte",
    "solid",
    "preact",
    "qwik",
    "lit",
    "web-component",
    "flutter",
    "svg",
  ];

  for (const format of allFormats) {
    it(`returns a generator function for ${format}`, () => {
      const gen = getGenerator(format);
      expect(typeof gen).toBe("function");
    });
  }
});

describe("mui generator", () => {
  const generate = getGenerator("mui");

  it("generates a createSymbolIcon call", () => {
    const output = generate(COMPONENT, SAMPLE_PATH, makeConfig());
    expect(output).toContain("createSymbolIcon");
    expect(output).toContain(`"${COMPONENT}"`);
    expect(output).toContain("export default createSymbolIcon(");
  });

  it("includes the 'use client' directive", () => {
    const output = generate(COMPONENT, SAMPLE_PATH, makeConfig());
    expect(output).toContain('"use client"');
  });

  it("omits 'use client' when disabled", () => {
    const output = generate(COMPONENT, SAMPLE_PATH, makeConfig({ useClientDirective: false }));
    expect(output).not.toContain('"use client"');
  });

  it("includes header comment", () => {
    const output = generate(COMPONENT, SAMPLE_PATH, makeConfig());
    expect(output).toContain("// Auto-generated");
  });

  it("omits header when disabled", () => {
    const output = generate(COMPONENT, SAMPLE_PATH, makeConfig({ header: false }));
    expect(output).not.toContain("// Auto-generated");
  });

  it("optimizes single-path SVGs to extract d attribute", () => {
    const singlePath = '<path d="M10 20v-6h4v6h5V10h3L12 3 2 10h3v10z"/>';
    const output = generate(COMPONENT, singlePath, makeConfig());
    expect(output).toContain('d="M10 20v-6h4v6h5V10h3L12 3 2 10h3v10z"');
    expect(output).not.toContain("<>");
  });

  it("wraps multi-path SVGs in Fragment", () => {
    const multiPath = '<path d="M10 20"/><path d="M5 5"/>';
    const output = generate(COMPONENT, multiPath, makeConfig());
    expect(output).toContain("<>");
    expect(output).toContain("</>");
  });

  it("imports createSymbolIcon from utils", () => {
    const output = generate(COMPONENT, SAMPLE_PATH, makeConfig());
    expect(output).toContain('import createSymbolIcon from "./utils/createSymbolIcon"');
  });
});

