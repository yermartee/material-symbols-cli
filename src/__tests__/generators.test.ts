describe("generateBarrel", () => {
  const icons = [
    { componentName: "Home", fileName: "Home" },
    { componentName: "Search", fileName: "Search" },
    { componentName: "ArrowBack", fileName: "ArrowBack" },
  ];

  it("generates default re-exports for mui", () => {
    const config = makeConfig({ extension: "tsx" });
    const output = generateBarrel(icons, config);
    expect(output).toContain('export { default as Home } from "./Home"');
    expect(output).toContain('export { default as Search } from "./Search"');
    expect(output).not.toContain("./Home.");
  });

  it("generates named exports for angular", () => {
    const config = makeConfig({ format: "angular", extension: "ts" });
    const output = generateBarrel(icons, config);
    expect(output).toContain('export { Home } from "./Home"');
    expect(output).not.toContain("default as");
  });

  it("preserves .vue extension in imports", () => {
    const config = makeConfig({ format: "vue", extension: "vue" });
    const output = generateBarrel(icons, config);
    expect(output).toContain('from "./Home.vue"');
  });

  it("preserves .svelte extension in imports", () => {
    const config = makeConfig({ format: "svelte", extension: "svelte" });
    const output = generateBarrel(icons, config);
    expect(output).toContain('from "./Home.svelte"');
  });

  it("preserves .svg extension for SVG format", () => {
    const config = makeConfig({ format: "svg", extension: "svg" });
    const output = generateBarrel(icons, config);
    expect(output).toContain('from "./Home.svg"');
  });

  it("uses named exports for qwik", () => {
    const config = makeConfig({ format: "qwik", extension: "tsx" });
    const output = generateBarrel(icons, config);
    expect(output).toContain("export { Home }");
    expect(output).not.toContain("default as");
  });

  it("uses named exports for lit", () => {
    const config = makeConfig({ format: "lit", extension: "ts" });
    const output = generateBarrel(icons, config);
    expect(output).toContain("export { Home }");
  });

  it("uses named exports for web-component", () => {
    const config = makeConfig({ format: "web-component", extension: "ts" });
    const output = generateBarrel(icons, config);
    expect(output).toContain("export { Home }");
  });

  it("includes header comment when configured", () => {
    const config = makeConfig();
    const output = generateBarrel(icons, config);
    expect(output).toContain("// Auto-generated");
  });

  it("omits header when disabled", () => {
    const config = makeConfig({ header: false });
    const output = generateBarrel(icons, config);
    expect(output).not.toContain("//");
  });
});

